std::string uint64_to_hex(uint64_t v) {
  std::ostringstream oss;
  oss << "0x" << std::hex << std::setw(16) << std::setfill('0') << v;
  return oss.str();
}

bool write_array_u64(std::ofstream& out, const std::string& name, const std::uint64_t* data,
                     size_t n) {
  out << "static inline const std::array<std::uint64_t, " << n << "> " << name << " = {\n    ";
  for (size_t i = 0; i < n; ++i) {
    out << uint64_to_hex(data[i]);
    if (i + 1 < n) out << ", ";
    if ((i + 1) % 4 == 0) out << "\n    ";
  }
  out << "\n};\n\n";
  return out.good();
}

bool write_array_u8(std::ofstream& out, const std::string& name, const uint8_t* data, size_t n) {
  out << "static inline const std::array<uint8_t, " << n << "> " << name << " = {\n    ";
  for (size_t i = 0; i < n; ++i) {
    out << static_cast<unsigned>(data[i]);
    if (i + 1 < n) out << ", ";
    if ((i + 1) % 16 == 0) out << "\n    ";
  }
  out << "\n};\n\n";
  return out.good();
}

bool write_table_u64_vec(std::ofstream& out, const std::string& name,
                         const std::vector<bb::Bitboard>* tables, size_t nTables) {
  out << "static inline const std::array<std::vector<std::uint64_t>, " << nTables << "> " << name
      << " = {\n";
  out << "    {\n";
  for (size_t i = 0; i < nTables; ++i) {
    const auto& vec = tables[i];
    out << "        std::vector<std::uint64_t>{";
    for (size_t j = 0; j < vec.size(); ++j) {
      out << uint64_to_hex(vec[j]);
      if (j + 1 < vec.size()) out << ", ";
    }
    out << "}";
    if (i + 1 < nTables)
      out << ",\n";
    else
      out << "\n";
  }
  out << "    }\n};\n\n";
  return out.good();
}

